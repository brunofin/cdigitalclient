package bean;

public class Foto {
	private int fotoId;
	private String local_foto;

	public int getFotoId() {
		return fotoId;
	}

	public void setFotoId(int id) {
		this.fotoId = id;
	}

	public String getLocal_foto() {
		return local_foto;
	}

	public void setLocal_foto(String local_foto) {
		this.local_foto = local_foto;
	}
}