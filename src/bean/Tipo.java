package bean;

public class Tipo {
	private int tipoId;
	private String nome;
	
	public int getTipoId() {
		return tipoId;
	}
	
	public void setTipoId(int tipoId) {
		this.tipoId = tipoId;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
}
